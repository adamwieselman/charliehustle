package com.charliehustle.models;

public class FighterInfoData {

    private String fighterId;
    private String fighterName;
    private String status;
    private String hometown;
    private String age;
    private String trainsAt;
    private String fightingStyle;
    private String height;
    private String weight;
    private String reach;
    private String legReach;

    public String getFighterId() {
        return fighterId;
    }

    public void setFighterId(String fighterId) {
        this.fighterId = fighterId;
    }

    public String getFighterName() {
        return fighterName;
    }

    public void setFighterName(String fighterName) {
        this.fighterName = fighterName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getHometown() {
        return hometown;
    }

    public void setHometown(String hometown) {
        this.hometown = hometown;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getTrainsAt() {
        return trainsAt;
    }

    public void setTrainsAt(String trainsAt) {
        this.trainsAt = trainsAt;
    }

    public String getFightingStyle() {
        return fightingStyle;
    }

    public void setFightingStyle(String fightingStyle) {
        this.fightingStyle = fightingStyle;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getReach() {
        return reach;
    }

    public void setReach(String reach) {
        this.reach = reach;
    }

    public String getLegReach() {
        return legReach;
    }

    public void setLegReach(String legReach) {
        this.legReach = legReach;
    }
}
